/* ===== Language Switcher ===== */
function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('asmo_lang', lang);
  document.documentElement.lang = lang === 'kk' ? 'kk' : lang === 'en' ? 'en' : 'ru';

  // Update text content
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  // Update active buttons in both switchers
  [langSwitcher, langSwitcherMobile].forEach(function(switcher) {
    if (switcher) {
      switcher.querySelectorAll('.lang-switcher__btn').forEach(function(btn) {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
      });
    }
  });
}

[langSwitcher, langSwitcherMobile].forEach(function(switcher) {
  if (switcher) {
    switcher.addEventListener('click', function(e) {
      var btn = e.target.closest('.lang-switcher__btn');
      if (btn) {
        setLanguage(btn.getAttribute('data-lang'));
      }
    });
  }
});

/* ===== Mobile Menu ===== */
// Create overlay element
var overlay = document.createElement('div');
overlay.className = 'nav-overlay';
document.body.appendChild(overlay);

function toggleMenu() {
  hamburger.classList.toggle('active');
  nav.classList.toggle('open');
  overlay.classList.toggle('visible');
  document.body.style.overflow = nav.classList.contains('open') ? 'hidden' : '';
}

function closeMenu() {
  hamburger.classList.remove('active');
  nav.classList.remove('open');
  overlay.classList.remove('visible');
  document.body.style.overflow = '';
}

hamburger.addEventListener('click', toggleMenu);
overlay.addEventListener('click', closeMenu);

// Close menu on nav link click
nav.querySelectorAll('.nav__link').forEach(function(link) {
  link.addEventListener('click', closeMenu);
});

/* ===== Sticky Header ===== */
var lastScroll = 0;

window.addEventListener('scroll', function() {
  var scroll = window.pageYOffset;
  header.classList.toggle('scrolled', scroll > 10);
  lastScroll = scroll;
}, { passive: true });

/* ===== Active Nav Highlight ===== */
var sections = document.querySelectorAll('section[id]');

function highlightNav() {
  var scrollPos = window.pageYOffset + 100;

  sections.forEach(function(section) {
    var top = section.offsetTop - 80;
    var height = section.offsetHeight;
    var id = section.getAttribute('id');

    if (scrollPos >= top && scrollPos < top + height) {
      nav.querySelectorAll('.nav__link').forEach(function(link) {
        link.classList.remove('active');
        if (link.getAttribute('href') === '#' + id) {
          link.classList.add('active');
        }
      });
    }
  });
}

window.addEventListener('scroll', highlightNav, { passive: true });

/* ===== Scroll Animations (Intersection Observer) ===== */
var observerOptions = {
  root: null,
  rootMargin: '0px 0px -60px 0px',
  threshold: 0.1
};

var observer = new IntersectionObserver(function(entries) {
  entries.forEach(function(entry) {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      observer.unobserve(entry.target);
    }
  });
}, observerOptions);

document.querySelectorAll('.animate-on-scroll').forEach(function(el) {
  observer.observe(el);
});

/* ===== Contact Form ===== */
contactForm.addEventListener('submit', function(e) {
  e.preventDefault();

  var formData = new FormData(contactForm);
  var data = {};
  formData.forEach(function(value, key) {
    data[key] = value;
  });

  // Simple validation
  if (!data.name || !data.phone || !data.email) return;

  // Visual feedback
  var btn = contactForm.querySelector('button[type="submit"]');
  var originalText = btn.textContent;
  btn.textContent = '✓';
  btn.style.background = '#0d9488';
  btn.disabled = true;

  setTimeout(function() {
    btn.textContent = originalText;
    btn.style.background = '';
    btn.disabled = false;
    contactForm.reset();
  }, 2000);
});

